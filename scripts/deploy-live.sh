if [ -z "$GITHUB_TOKEN" ]; then
    echo "âŒ GITHUB_TOKEN not set. Please set it first:"
    echo "export GITHUB_TOKEN=\"your_token_here\""
    exit 1
fi

echo "âœ… GitHub token is set"
echo "ğŸ“ Reading generated README content..."

# Read the generated README
readme_content=$(cat "/Users/Mea/code/github-ecosystem/ecosystem-central-command/generated-readmes/external-learning-platforms-README.md")

if [ -z "$readme_content" ]; then
    echo "âŒ Generated README not found. Run universal-readme-manager.js first."
    exit 1
fi

echo "âœ… README content loaded (${#readme_content} characters)"

# Get current README SHA (required for updates)
echo "ğŸ” Getting current README information..."
current_readme=$(curl -s -H "Authorization: token $GITHUB_TOKEN" \
    "https://api.github.com/repos/DevPersonalHub/external-learning-platforms/contents/README.md")

sha=$(echo "$current_readme" | grep '"sha"' | sed 's/.*"sha": *"\([^"]*\)".*/\1/')

if [ -z "$sha" ]; then
    echo "ğŸ“ No existing README found - will create new one"
    create_mode=true
else
    echo "âœ… Found existing README (SHA: ${sha:0:7}...)"
    create_mode=false
fi

# Encode content to base64 (required by GitHub API)
echo "ğŸ”„ Encoding content for GitHub API..."
encoded_content=$(echo "$readme_content" | base64)

# Prepare API payload
if [ "$create_mode" = true ]; then
    # Create new README
    payload=$(cat <<EOF
{
  "message": "ğŸ“ Add comprehensive external learning platforms README

âœ¨ Features:
- Enhanced repository structure with emoji visualization
