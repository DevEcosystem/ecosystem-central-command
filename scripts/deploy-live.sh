)
else
    # Update existing README
    payload=$(cat <<EOF
{
  "message": "ğŸš€ Update external learning platforms README

âœ¨ Enhanced with:
- Emoji-enhanced repository structure visualization
- Updated project counts and technology stacks
- Comprehensive learning analytics
- Professional presentation improvements

ğŸ¤– Auto-generated by Universal README Management",
  "content": "$encoded_content",
  "sha": "$sha"
}
EOF
)
fi

echo "ğŸš€ Deploying to GitHub..."

# Deploy to GitHub
response=$(curl -s -X PUT \
    -H "Authorization: token $GITHUB_TOKEN" \
    -H "Accept: application/vnd.github.v3+json" \
    -d "$payload" \
    "https://api.github.com/repos/DevPersonalHub/external-learning-platforms/contents/README.md")

# Check if deployment was successful
if echo "$response" | grep -q '"commit"'; then
    echo "âœ… SUCCESS! README deployed to DevPersonalHub/external-learning-platforms"
    echo "ğŸŒ View at: https://github.com/DevPersonalHub/external-learning-platforms"
